const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const countryValidation = require('../../validations/country.validation');
const countryController = require('../../controllers/country.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageCountrys'), validate(countryValidation.createCountry), countryController.createCountry)
  .get(auth('getCountrys'), validate(countryValidation.getCountrys), countryController.getCountrys);

router
  .route('/:countryId')
  .get(auth('getCountrys'), validate(countryValidation.getCountry), countryController.getCountry)
  .patch(auth('manageCountrys'), validate(countryValidation.updateCountry), countryController.updateCountry)
  .delete(auth('manageCountrys'), validate(countryValidation.deleteCountry), countryController.deleteCountry);

module.exports = router;

