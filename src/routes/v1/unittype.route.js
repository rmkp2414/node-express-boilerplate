const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const unittypeValidation = require('../../validations/unittype.validation');
const unittypeController = require('../../controllers/unittype.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageUnitTypes'), validate(unittypeValidation.createUnitType), unittypeController.createUnitType)
  .get(auth('getUnitTypes'), validate(unittypeValidation.getUnitTypes), unittypeController.getUnitTypes);

router
  .route('/:unittypeId')
  .get(auth('getUnitTypes'), validate(unittypeValidation.getUnitType), unittypeController.getUnitType)
  .patch(auth('manageUnitTypes'), validate(unittypeValidation.updateUnitType), unittypeController.updateUnitType)
  .delete(auth('manageUnitTypes'), validate(unittypeValidation.deleteUnitType), unittypeController.deleteUnitType);

module.exports = router;

