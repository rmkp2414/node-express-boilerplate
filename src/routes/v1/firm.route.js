const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const firmValidation = require('../../validations/firm.validation');
const firmController = require('../../controllers/firm.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageFirms'), validate(firmValidation.createFirm), firmController.createFirm)
  .get(auth('getFirms'), validate(firmValidation.getFirms), firmController.getFirms);

router
  .route('/:firmId')
  .get(auth('getFirms'), validate(firmValidation.getFirm), firmController.getFirm)
  .patch(auth('manageFirms'), validate(firmValidation.updateFirm), firmController.updateFirm)
  .delete(auth('manageFirms'), validate(firmValidation.deleteFirm), firmController.deleteFirm);

module.exports = router;

