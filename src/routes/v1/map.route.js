const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const mapValidation = require('../../validations/map.validation');
const mapController = require('../../controllers/map.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageMaps'), validate(mapValidation.createMap), mapController.createMap)
  .get(auth('getMaps'), validate(mapValidation.getMaps), mapController.getMaps);

router
  .route('/:mapId')
  .get(auth('getMaps'), validate(mapValidation.getMap), mapController.getMap)
  .patch(auth('manageMaps'), validate(mapValidation.updateMap), mapController.updateMap)
  .delete(auth('manageMaps'), validate(mapValidation.deleteMap), mapController.deleteMap);

module.exports = router;

